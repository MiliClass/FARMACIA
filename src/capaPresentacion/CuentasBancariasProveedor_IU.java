    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (txtRuc.getText().length() > 0) {
            DetalleCuentaBancariasBD oCuentas = new DetalleCuentaBancariasBD();
            ProveedorBD oProveedorBD = new ProveedorBD();
            tabla_temporal_proveedor = oProveedorBD.buscarProveedor(txtRuc.getText().trim());
            if (tabla_temporal_proveedor.getRowCount() > 0) {
                txtRazonSocial.setText(tabla_temporal_proveedor.getValueAt(0, 1).toString());
                tabla_temporal_detalle_cuenta = oCuentas.buscarDetalleCuentasBancarias(txtRuc.getText().trim());
                tabla_reporte_cuentas_bancarias.setModel(tabla_temporal_detalle_cuenta);

            } else {
                error("No se encontró al proveedor, tienes que registrarlo primero...");
                txtRuc.requestFocus();
                txtRuc.setText("");
                txtRazonSocial.setText("");
                tabla_temporal_detalle_cuenta = oCuentas.buscarDetalleCuentasBancarias(txtRuc.getText().trim());
                tabla_reporte_cuentas_bancarias.setModel(tabla_temporal_detalle_cuenta);
                
            }

        } else {
            advertencia("Ingrese el RUC para buscar al proveedor");
            txtRuc.requestFocus();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if (txtBanco.getText().length() > 0) {
            if (txtNroCuenta.getText().length() > 0) {
                if (txtCuenta.getText().length() > 0) {
                    DetalleCuentasBancarias oDetalleCuentaBancarias= new DetalleCuentasBancarias();
                    DetalleCuentaBancariasBD oDetalleCuentaBancariasBD= new DetalleCuentaBancariasBD();
                    oDetalleCuentaBancarias.setBanco(txtBanco.getText().toUpperCase().trim());
                    oDetalleCuentaBancarias.setNroCuenta(txtNroCuenta.getText().toUpperCase().trim());
                    oDetalleCuentaBancarias.setCuenta(txtCuenta.getText().toUpperCase().trim());
                    oDetalleCuentaBancarias.setProvRuc(txtRuc.getText().trim());
                    boolean rpta= oDetalleCuentaBancariasBD.registrarDetalleCuentasBancarias(oDetalleCuentaBancarias);
                  
                                        if (rpta) {
                                            exito("Se registro con exito");
                                           DefaultTableModel tabla_temporal;
                                            DetalleCuentaBancariasBD oCuentas = new DetalleCuentaBancariasBD();
                                            tabla_temporal= oCuentas.buscarDetalleCuentasBancarias(txtRuc.getText().trim());
                                            tabla_reporte_cuentas_bancarias.setModel(tabla_temporal);
                                           
                                            limpiar();
                                        } else {
                                            error("Tiene problemas al registrar");
                                        }

                } else {
                    advertencia("Ingrese la cuenta");
                    txtCuenta.requestFocus();
                }

            } else {
                advertencia("Ingrese el nro de cuenta");
                txtNroCuenta.requestFocus();
            }
        } else {
            advertencia("Ingrese el nombre del banco");
            txtBanco.requestFocus();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

