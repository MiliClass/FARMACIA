    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int aviso = JOptionPane.showConfirmDialog(rootPane, "Estás seguro de eliminar?");
                if (aviso == 0) {
                    DetalleCuentaBancariasBD oDetalleCuentaBancariasBD= new DetalleCuentaBancariasBD();
                   
                    boolean rpta = oDetalleCuentaBancariasBD.eliminarDetalleCuentaBancarias(idcuentas);
                    if (rpta) {
                        exito("Todo OK");
                        
                        limpiar();
                        limpiar_tabla_formulario();
                    } else {
                        error("Tiene problemas al eliminar");
                    }
                }
    }//GEN-LAST:event_btnEliminarActionPerformed

